def whileLoops() : 
    i = 1
    while i < 5 :                               #This will execute till 1-4 
        print("...... inside loop ......", i)   # While loop will keep executing until while condtion becomes false
        i = i + 1

    print(">>>>>> outside loop >>>>>>", i)

#whileLoops()

