def range_1():
    for x in range(3):          # range(start, end) range will start from 0 and end to 3-1 i.e. 2 
        print(x)
                                     
    for y in range(5,10+1):     # range(start, end+1) We can use +1 in range
        print(y)

    for z in range(2*2, 25, 3+2):       # we can use math expressions in range                         
        print(z)
          
    for a in range(10, 0, -2):         # negative values are also supported in range step parameter                   
        print(a)                     


    print(range(5))                     # range function does not work with print


range_1()
